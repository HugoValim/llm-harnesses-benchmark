"""Publish benchmark campaigns to git with curated audit and result artifacts."""

from __future__ import annotations

import json
import os
import re
import shutil
from dataclasses import dataclass
from datetime import date
from pathlib import Path

from benchmark.result_layout import HARNESS_PREFIXES, split_target_slug
from benchmark.util import file_sha256

GITIGNORE_BEGIN = "# BEGIN published-campaigns (generated by scripts/publish_campaign.py)"
GITIGNORE_END = "# END published-campaigns"

PROJECT_EPHEMERAL_DIR_NAMES: frozenset[str] = frozenset(
    {
        ".venv",
        "venv",
        "node_modules",
        "twcli",
        "tailwindcss",
        "_runtime_verification",
        "__pycache__",
        ".pytest_cache",
        ".mypy_cache",
        ".ruff_cache",
        ".git",
        "staticfiles",
    }
)

PROJECT_EPHEMERAL_FILE_NAMES: frozenset[str] = frozenset({".coverage", ".env"})

TAILWINDCSS_BINARY_PREFIX = "tailwindcss-"

AUDIT_EXCLUDE_DIR_NAMES: frozenset[str] = frozenset({"_meta-analysis-runs"})

AUDIT_EXCLUDE_FILE_NAMES: frozenset[str] = frozenset(
    {
        "stream.ndjson",
        "stderr.log",
        "prompt.txt",
        "AGENTS.md",
        "CLAUDE.md",
    }
)

RESULT_EXCLUDE_FILE_SUFFIXES: tuple[str, ...] = (".ndjson",)
RESULT_EXCLUDE_FILE_NAMES: frozenset[str] = frozenset(
    {
        "stderr.log",
        "followup-stderr.log",
        "followup-opencode-stderr.log",
        "session-export.json",
        "session-export.stderr.log",
        "opencode-stderr.log",
    }
)

SECRET_PATTERNS: tuple[re.Pattern[str], ...] = (
    re.compile(r"sk-[A-Za-z0-9]{20,}"),
    re.compile(r"OPENROUTER_API_KEY\s*=\s*['\"][^'\"]+['\"]"),
    re.compile(r"ANTHROPIC_API_KEY\s*=\s*['\"][^'\"]+['\"]"),
)


@dataclass(frozen=True)
class CampaignManifest:
    id: str
    label: str
    date: str
    prompt_versions: dict[str, str]
    auditor_slug: str
    meta_analysis: str
    audit_root: str
    benchmark_results_root: str
    targets: list[str]
    harnesses: list[str]
    models_config_sha256: str
    harnesses_config_sha256: str
    run_id: str | None = None

    def to_dict(self) -> dict[str, object]:
        payload: dict[str, object] = {
            "id": self.id,
            "label": self.label,
            "date": self.date,
            "prompt_versions": self.prompt_versions,
            "auditor_slug": self.auditor_slug,
            "meta_analysis": self.meta_analysis,
            "audit_root": self.audit_root,
            "benchmark_results_root": self.benchmark_results_root,
            "targets": self.targets,
            "harnesses": self.harnesses,
            "models_config_sha256": self.models_config_sha256,
            "harnesses_config_sha256": self.harnesses_config_sha256,
        }
        if self.run_id is not None:
            payload["run_id"] = self.run_id
        return payload

    @classmethod
    def from_dict(cls, payload: dict[str, object]) -> CampaignManifest:
        prompt_versions = payload.get("prompt_versions", {})
        targets = payload.get("targets", [])
        harnesses = payload.get("harnesses", [])
        if not isinstance(prompt_versions, dict):
            raise ValueError(
                f"manifest prompt_versions must be a dict, got {type(prompt_versions)!r}"
            )
        if not isinstance(targets, list) or not all(isinstance(t, str) for t in targets):
            raise ValueError(
                f"manifest targets must be a list[str], got {targets!r}"
            )
        if not isinstance(harnesses, list) or not all(
            isinstance(h, str) for h in harnesses
        ):
            raise ValueError(
                f"manifest harnesses must be a list[str], got {harnesses!r}"
            )
        return cls(
            id=str(payload["id"]),
            label=str(payload["label"]),
            date=str(payload["date"]),
            prompt_versions={str(k): str(v) for k, v in prompt_versions.items()},
            auditor_slug=str(payload["auditor_slug"]),
            meta_analysis=str(payload["meta_analysis"]),
            audit_root=str(payload["audit_root"]),
            benchmark_results_root=str(payload["benchmark_results_root"]),
            targets=list(targets),
            harnesses=list(harnesses),
            models_config_sha256=str(payload["models_config_sha256"]),
            harnesses_config_sha256=str(payload["harnesses_config_sha256"]),
            run_id=str(payload["run_id"]) if payload.get("run_id") else None,
        )


def read_prompt_version(path: Path) -> str:
    """Return the Prompt-Version header from a prompt template file."""
    if not path.exists():
        return "unknown"
    for line in path.read_text(encoding="utf-8").splitlines()[:5]:
        if line.startswith("Prompt-Version:"):
            return line.split(":", 1)[1].strip()
    return "unknown"


def discover_targets_from_auditor(audit_root: Path, auditor_slug: str) -> list[str]:
    """List target slugs with report.md under audit-reports/<auditor>/."""
    auditor_dir = audit_root / auditor_slug
    if not auditor_dir.is_dir():
        raise FileNotFoundError(
            f"auditor directory does not exist: {auditor_dir}"
        )
    targets: list[str] = []
    for child in sorted(auditor_dir.iterdir()):
        if not child.is_dir():
            continue
        if child.name in AUDIT_EXCLUDE_DIR_NAMES:
            continue
        if (child / "report.md").is_file():
            targets.append(child.name)
    if not targets:
        raise ValueError(
            f"no audit targets with report.md under {auditor_dir}"
        )
    return targets


def infer_harnesses(targets: list[str]) -> list[str]:
    """Return sorted harness prefixes present in target directory names."""
    found: set[str] = set()
    for target in targets:
        harness, _ = split_target_slug(target)
        if harness is not None:
            found.add(harness)
    return sorted(found, key=lambda h: HARNESS_PREFIXES.index(h) if h in HARNESS_PREFIXES else 99)


def build_manifest(
    *,
    campaign_id: str,
    label: str,
    auditor_slug: str,
    targets: list[str],
    repo_root: Path,
    campaign_date: str | None = None,
    benchmark_results_root: str = "results",
    audit_root: str = "audit-reports",
    run_id: str | None = None,
) -> CampaignManifest:
    """Assemble a campaign manifest from repo paths and discovered targets."""
    prompts_dir = repo_root / "prompts"
    models_config = repo_root / "config" / "models.json"
    harnesses_config = repo_root / "config" / "harnesses.json"

    if run_id is not None:
        benchmark_results_root = f"results/{run_id}/projects"
        audit_root = f"results/{run_id}/audit-reports/{auditor_slug}"
        meta_analysis = f"results/{run_id}/meta-analysis.md"
    else:
        audit_root_path = repo_root / audit_root
        meta_analysis = f"{audit_root}/{auditor_slug}/meta-analysis.md"
        audit_root = f"{audit_root}/{auditor_slug}"

    return CampaignManifest(
        id=campaign_id,
        label=label,
        date=campaign_date or date.today().isoformat(),
        prompt_versions={
            "benchmark": read_prompt_version(prompts_dir / "benchmark_prompt.txt"),
            "benchmark_followup": read_prompt_version(
                prompts_dir / "benchmark_followup_prompt.txt"
            ),
            "audit": read_prompt_version(prompts_dir / "audit_prompt_template.txt"),
            "meta": read_prompt_version(
                prompts_dir / "audit_meta_analysis_prompt.txt"
            ),
        },
        auditor_slug=auditor_slug,
        meta_analysis=meta_analysis,
        audit_root=audit_root,
        benchmark_results_root=benchmark_results_root,
        targets=sorted(targets),
        harnesses=infer_harnesses(targets),
        models_config_sha256=file_sha256(models_config),
        harnesses_config_sha256=file_sha256(harnesses_config),
        run_id=run_id,
    )


def _is_ephemeral_project_dir(name: str) -> bool:
    if name in PROJECT_EPHEMERAL_DIR_NAMES:
        return True
    return name.startswith(".venv")


def strip_project_ephemeral(project_dir: Path) -> list[str]:
    """Remove ephemeral directories/files from a generated project workspace."""
    removed: list[str] = []
    if not project_dir.is_dir():
        return removed

    for child in project_dir.iterdir():
        if child.is_dir() and _is_ephemeral_project_dir(child.name):
            shutil.rmtree(child)
            removed.append(str(child.relative_to(project_dir)))

    for name in PROJECT_EPHEMERAL_DIR_NAMES:
        path = project_dir / name
        if path.exists() and str(path.relative_to(project_dir)) not in removed:
            if path.is_dir():
                shutil.rmtree(path)
            else:
                path.unlink()
            removed.append(str(path.relative_to(project_dir)))

    for path in project_dir.rglob("*.pyc"):
        path.unlink(missing_ok=True)
        removed.append(str(path.relative_to(project_dir)))

    db_path = project_dir / "db.sqlite3"
    if db_path.is_file():
        db_path.unlink()
        removed.append(str(db_path.relative_to(project_dir)))

    for name in PROJECT_EPHEMERAL_FILE_NAMES:
        path = project_dir / name
        if path.is_file():
            path.unlink()
            removed.append(str(path.relative_to(project_dir)))

    for path in project_dir.glob("*.log"):
        if path.is_file():
            path.unlink()
            removed.append(str(path.relative_to(project_dir)))

    for path in project_dir.iterdir():
        if path.is_file() and path.name.startswith(TAILWINDCSS_BINARY_PREFIX):
            path.unlink()
            removed.append(str(path.relative_to(project_dir)))

    return removed


def strip_result_ephemeral(result_dir: Path) -> list[str]:
    """Remove ephemeral files from a benchmark result directory."""
    removed: list[str] = []
    if not result_dir.is_dir():
        return removed

    for path in result_dir.iterdir():
        if not path.is_file():
            continue
        if path.suffix in RESULT_EXCLUDE_FILE_SUFFIXES:
            path.unlink()
            removed.append(str(path.relative_to(result_dir)))
            continue
        if path.name in RESULT_EXCLUDE_FILE_NAMES:
            path.unlink()
            removed.append(str(path.relative_to(result_dir)))

    return removed


def strip_campaign_results(repo_root: Path, manifest: CampaignManifest) -> dict[str, list[str]]:
    """Strip ephemeral artifacts from each published benchmark result."""
    results_root = repo_root / manifest.benchmark_results_root
    stripped: dict[str, list[str]] = {}
    for target in manifest.targets:
        result_dir = results_root / target
        removed = strip_result_ephemeral(result_dir)
        project_dir = result_dir / "project"
        removed.extend(strip_project_ephemeral(project_dir))
        if removed:
            stripped[target] = removed
    return stripped


def render_gitignore_block(manifest: CampaignManifest) -> str:
    """Render the generated .gitignore section for one published campaign."""
    results_root = manifest.benchmark_results_root
    lines = [
        GITIGNORE_BEGIN,
        f"# Campaign: {manifest.id} ({manifest.label})",
        "",
        "# Default: ignore all runtime outputs",
        f"/{results_root}/*/",
        "/results-claude-code/*/",
        f"/{results_root}/runtime_verification_summary.json",
        "/audit-reports/*/",
        "",
        f"# Published audit tree: {manifest.audit_root}",
        f"!/{manifest.audit_root}/",
        f"/{manifest.audit_root}/_meta-analysis-runs/",
    ]
    for name in sorted(AUDIT_EXCLUDE_FILE_NAMES):
        lines.append(f"/{manifest.audit_root}/**/{name}")

    lines.extend(
        [
            "",
            "# Published benchmark targets (allowlist)",
        ]
    )
    for target in manifest.targets:
        lines.append(f"!/{results_root}/{target}/")

    lines.extend(
        [
            "",
            "# Shared exclusions for published benchmark targets",
            f"/{results_root}/**/*.ndjson",
            f"/{results_root}/**/stderr.log",
            f"/{results_root}/**/followup-stderr.log",
            f"/{results_root}/**/followup-opencode-stderr.log",
            f"/{results_root}/**/opencode-stderr.log",
            f"/{results_root}/**/session-export.json",
            f"/{results_root}/**/session-export.stderr.log",
            f"/{results_root}/**/project/.venv/",
            f"/{results_root}/**/project/.venv*/",
            f"/{results_root}/**/project/venv/",
            f"/{results_root}/**/project/node_modules/",
            f"/{results_root}/**/project/twcli/",
            f"/{results_root}/**/project/tailwindcss/",
            f"/{results_root}/**/project/{TAILWINDCSS_BINARY_PREFIX}*",
            f"/{results_root}/**/project/staticfiles/",
            f"/{results_root}/**/project/.git/",
            f"/{results_root}/**/project/.mypy_cache/",
            f"/{results_root}/**/project/.ruff_cache/",
            f"/{results_root}/**/project/.coverage",
            f"/{results_root}/**/project/_runtime_verification/",
            f"/{results_root}/**/project/__pycache__/",
            f"/{results_root}/**/project/.pytest_cache/",
            f"/{results_root}/**/project/db.sqlite3",
            f"/{results_root}/**/project/.env",
            f"/{results_root}/**/project/**/*.log",
            "",
        ]
    )

    lines.append(GITIGNORE_END)
    return "\n".join(lines) + "\n"


def update_gitignore(repo_root: Path, block: str) -> None:
    """Replace or append the generated published-campaign block in .gitignore."""
    gitignore_path = repo_root / ".gitignore"
    existing = gitignore_path.read_text(encoding="utf-8") if gitignore_path.exists() else ""

    if GITIGNORE_BEGIN in existing and GITIGNORE_END in existing:
        before, rest = existing.split(GITIGNORE_BEGIN, 1)
        _, after = rest.split(GITIGNORE_END, 1)
        updated = before.rstrip() + "\n\n" + block.rstrip() + "\n" + after.lstrip("\n")
    else:
        trimmed = existing.rstrip()
        updated = (trimmed + "\n\n" + block) if trimmed else block

    gitignore_path.write_text(updated, encoding="utf-8")


def update_symlink(link_path: Path, target: Path) -> None:
    """Create or replace a symlink relative to link_path.parent."""
    rel_target = Path(os.path.relpath(target, link_path.parent))  # type: ignore[name-defined]
    if link_path.is_symlink() or link_path.exists():
        link_path.unlink()
    link_path.symlink_to(rel_target)


def write_manifest(manifest_path: Path, manifest: CampaignManifest) -> None:
    manifest_path.parent.mkdir(parents=True, exist_ok=True)
    manifest_path.write_text(
        json.dumps(manifest.to_dict(), indent=2, sort_keys=False) + "\n",
        encoding="utf-8",
    )


def load_manifest(manifest_path: Path) -> CampaignManifest:
    payload = json.loads(manifest_path.read_text(encoding="utf-8"))
    if not isinstance(payload, dict):
        raise ValueError(
            f"manifest must be a JSON object, got {type(payload)!r}: {manifest_path}"
        )
    return CampaignManifest.from_dict(payload)


def collect_publish_paths(repo_root: Path, manifest: CampaignManifest) -> list[Path]:
    """Return repo-relative paths intended for git publication."""
    paths: list[Path] = []
    audit_dir = repo_root / manifest.audit_root
    comparison_path = audit_dir / "comparison.md"
    if comparison_path.is_file():
        paths.append(comparison_path)

    if manifest.run_id is not None:
        meta_path = repo_root / manifest.meta_analysis
        if meta_path.is_file():
            paths.append(meta_path)
    else:
        for rel in ("meta-analysis.md", "comparison.md"):
            path = audit_dir / rel
            if path.is_file():
                paths.append(path)

    for target in manifest.targets:
        target_dir = audit_dir / target
        for rel in ("report.md", "generation-metrics.json", "result.json"):
            path = target_dir / rel
            if path.is_file():
                paths.append(path)

        result_dir = repo_root / manifest.benchmark_results_root / target
        for rel in ("result.json", "prompt.txt", "followup-prompt.txt"):
            path = result_dir / rel
            if path.is_file():
                paths.append(path)

        project_dir = result_dir / "project"
        if project_dir.is_dir():
            for path in project_dir.rglob("*"):
                if path.is_file() and _project_file_publishable(path, project_dir):
                    paths.append(path)

    return paths


def _project_file_publishable(path: Path, project_root: Path) -> bool:
    rel = path.relative_to(project_root)
    for part in rel.parts:
        if _is_ephemeral_project_dir(part):
            return False
        if part in PROJECT_EPHEMERAL_DIR_NAMES:
            return False
    if rel.name == "db.sqlite3" or rel.suffix == ".pyc":
        return False
    if rel.name in {"AGENTS.md", "CLAUDE.md", ".env"}:
        return False
    if rel.name.startswith(TAILWINDCSS_BINARY_PREFIX):
        return False
    if rel.suffix == ".log":
        return False
    return True


def validate_no_secrets(paths: list[Path]) -> list[str]:
    """Scan publishable text files for obvious secret patterns."""
    errors: list[str] = []
    for path in paths:
        if path.suffix not in {".py", ".md", ".txt", ".json", ".yml", ".yaml", ".toml", ".env", ".example"}:
            continue
        try:
            text = path.read_text(encoding="utf-8", errors="replace")
        except OSError as exc:
            errors.append(f"{path}: unreadable ({exc})")
            continue
        for pattern in SECRET_PATTERNS:
            if pattern.search(text):
                errors.append(
                    f"{path}: matched secret pattern {pattern.pattern!r}"
                )
    return errors


def update_latest_symlinks(
    repo_root: Path,
    *,
    campaign_id: str,
    auditor_slug: str,
    run_id: str | None = None,
) -> None:
    """Point data/campaigns/latest and results/latest at the campaign."""
    campaigns_root = repo_root / "data" / "campaigns"
    campaigns_root.mkdir(parents=True, exist_ok=True)
    update_symlink(campaigns_root / "latest", campaigns_root / campaign_id)

    results_root = repo_root / "results"
    results_root.mkdir(parents=True, exist_ok=True)
    if run_id is not None:
        update_symlink(results_root / "latest", results_root / run_id)
    else:
        audit_reports_root = repo_root / "audit-reports"
        audit_reports_root.mkdir(parents=True, exist_ok=True)
        update_symlink(audit_reports_root / "latest", audit_reports_root / auditor_slug)


def publish_campaign(
    *,
    repo_root: Path,
    manifest: CampaignManifest,
    strip_results: bool = True,
    update_gitignore_file: bool = True,
    update_symlinks: bool = True,
) -> dict[str, object]:
    """Publish one campaign: strip, write manifest, gitignore, symlinks."""
    manifest_path = (
        repo_root / "data" / "campaigns" / manifest.id / "manifest.json"
    )
    write_manifest(manifest_path, manifest)

    stripped: dict[str, list[str]] = {}
    if strip_results:
        stripped = strip_campaign_results(repo_root, manifest)

    if update_gitignore_file:
        update_gitignore(repo_root, render_gitignore_block(manifest))

    if update_symlinks:
        update_latest_symlinks(
            repo_root,
            campaign_id=manifest.id,
            auditor_slug=manifest.auditor_slug,
            run_id=manifest.run_id,
        )

    publish_paths = collect_publish_paths(repo_root, manifest)
    secret_errors = validate_no_secrets(publish_paths)

    return {
        "manifest_path": manifest_path,
        "stripped": stripped,
        "publish_paths": publish_paths,
        "secret_errors": secret_errors,
    }
